public class Aritmetica {
    // aqui creamos los metodos que es para sumar y se puede variar los parametros
    
    public int sumar(int op1,int op2 ,int op3 ){
        // ejecuta o retorna el resultado o metodo  
        return op1+op2+op3;
                   
    }
   // y si quiera mas operaciones creamos mas metodos como ven a continuacion
    public int restar(int op1,int op2){
        return op1-op2;
}
    public int Multiplicar(int op3,int op4){
        return op3*op4;
    }
    public int dividir(int op3,int op4){
        return op3/op4;
        // Nota: tienen q escribir los metodos dentro de public class Aritmetica
        //atributos ninguno
        //metodos 4
    }
    
}